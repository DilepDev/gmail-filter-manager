class Label:

    def setName(self, name):
        self.name = name

    def setmessageListVisibility(self, messageListVisibility):
        self.messageListVisibility = messageListVisibility

    def setLabelListVisibility(self, labelListVisibility):
        self.labelListVisibility = labelListVisibility

    def setBackgroundColor(self, backgroundColor):
        self.backgroundColor = backgroundColor

    def setTextColor(self, textColor):
        self.textColor = textColor

    def getName(self):
        return self.name

    def getmessageListVisibility(self):
        return self.messageListVisibility

    def getLabelListVisibility(self):
        return self.labelListVisibility

    def getBackgroundColor(self):
        return self.backgroundColor

    def getTextColor(self):
        return self.textColor